// –ù–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è –æ–±—Ä–æ–±–Ω–∏–∫—ñ–≤ –ø–æ–¥—ñ–π –¥–ª—è —Å–∏—Å—Ç–µ–º–∏ –ø—ñ–¥–∫–∞–∑–æ–∫
function setupHintEventListeners() {
  // –í—ñ–¥–∫—Ä–∏—Ç—Ç—è –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –≤—ñ–∫–Ω–∞ –ø—Ä–∏ –Ω–∞—Ç–∏—Å–∫–∞–Ω–Ω—ñ –Ω–∞ –∫–Ω–æ–ø–∫—É –ø—ñ–¥–∫–∞–∑–∫–∏
  document.querySelectorAll(".hint-btn").forEach((button) => {
    button.addEventListener("click", (e) => {
      const modalId = button.getAttribute("data-target");
      const modal = document.getElementById(modalId);
      if (modal) {
        modal.classList.add("active");
        updateHintProgress(modalId.replace("hint-modal-", ""));
        e.stopPropagation();
      }
    });
  });

  // –ó–∞–∫—Ä–∏—Ç—Ç—è –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –≤—ñ–∫–Ω–∞ –ø—Ä–∏ –Ω–∞—Ç–∏—Å–∫–∞–Ω–Ω—ñ –Ω–∞ –∫–Ω–æ–ø–∫—É –∑–∞–∫—Ä–∏—Ç—Ç—è
  document.querySelectorAll(".hint-close-btn").forEach((button) => {
    button.addEventListener("click", () => {
      const modal = button.closest(".hint-modal");
      if (modal) {
        modal.classList.remove("active");
      }
    });
  });

  // –ó–∞–∫—Ä–∏—Ç—Ç—è –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –≤—ñ–∫–Ω–∞ –ø—Ä–∏ –Ω–∞—Ç–∏—Å–∫–∞–Ω–Ω—ñ –∑–∞ –π–æ–≥–æ –º–µ–∂–∞–º–∏
  document.querySelectorAll(".hint-modal").forEach((modal) => {
    modal.addEventListener("click", (e) => {
      if (e.target === modal) {
        modal.classList.remove("active");
      }
    });
  });

  // –ó–∞–ø–æ–±—ñ–≥–∞–Ω–Ω—è –∑–∞–∫—Ä–∏—Ç—Ç—é –ø—Ä–∏ –Ω–∞—Ç–∏—Å–∫–∞–Ω–Ω—ñ –≤—Å–µ—Ä–µ–¥–∏–Ω—ñ –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –≤—ñ–∫–Ω–∞
  document.querySelectorAll(".hint-modal-content").forEach((content) => {
    content.addEventListener("click", (e) => {
      e.stopPropagation();
    });
  });
}

// –û–Ω–æ–≤–ª–µ–Ω–Ω—è –ø—Ä–æ–≥—Ä–µ—Å—É –¥–ª—è –ø—ñ–¥–∫–∞–∑–∫–∏
function updateHintProgress(botId) {
  if (!hintsConfig[botId] || !chatBots[botId]) return;

  const modal = document.getElementById(`hint-modal-${botId}`);
  if (!modal) return;

  const progressBar = modal.querySelector(".hint-progress-fill");
  const progressText = modal.querySelector(".hint-progress-text");

  // –ê–Ω–∞–ª—ñ–∑ –ø–æ—Ç–æ—á–Ω–æ–≥–æ —Å—Ç–∞–Ω—É —Ä–æ–∑–º–æ–≤–∏
  const successPhrases = hintsConfig[botId].successPhrases;
  const keywords = hintsConfig[botId].keywords.map((k) => k.word.toLowerCase());

  let mentionedKeywords = 0;
  let successResponses = 0;

  // –ü—ñ–¥—Ä–∞—Ö—É–Ω–æ–∫ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–∏—Ö –∫–ª—é—á–æ–≤–∏—Ö —Å–ª—ñ–≤ —ñ —É—Å–ø—ñ—à–Ω–∏—Ö –≤—ñ–¥–ø–æ–≤—ñ–¥–µ–π
  chatBots[botId].memory.forEach((message) => {
    if (message.sender === "user") {
      const lowerText = message.text.toLowerCase();
      keywords.forEach((keyword) => {
        if (lowerText.includes(keyword.toLowerCase())) {
          mentionedKeywords++;
        }
      });
    } else if (message.sender === "ai") {
      const lowerText = message.text.toLowerCase();
      successPhrases.forEach((phrase) => {
        if (lowerText.includes(phrase.toLowerCase())) {
          successResponses++;
        }
      });
    }
  });

  // –†–æ–∑—Ä–∞—Ö—É–Ω–æ–∫ –ø—Ä–æ–≥—Ä–µ—Å—É (–≤—ñ–¥ 0 –¥–æ 100%)
  // –§–æ—Ä–º—É–ª–∞: (–≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω—ñ –∫–ª—é—á–æ–≤—ñ —Å–ª–æ–≤–∞ / 2 + —É—Å–ø—ñ—à–Ω—ñ –≤—ñ–¥–ø–æ–≤—ñ–¥—ñ) / (–∫—ñ–ª—å–∫—ñ—Å—Ç—å –∫–ª—é—á–æ–≤–∏—Ö —Å–ª—ñ–≤ / 2 + 3) * 100%
  const maxKeywords = keywords.length;
  const maxResponses = 3;
  const progress = Math.min(
    100,
    Math.round(
      ((mentionedKeywords / 2 + successResponses) /
        (maxKeywords / 2 + maxResponses)) *
        100
    )
  );

  // –û–Ω–æ–≤–ª–µ–Ω–Ω—è –ø—Ä–æ–≥—Ä–µ—Å-–±–∞—Ä—É
  progressBar.style.width = `${progress}%`;

  // –û–Ω–æ–≤–ª–µ–Ω–Ω—è —Ç–µ–∫—Å—Ç—É –ø—Ä–æ–≥—Ä–µ—Å—É
  let statusText = "";
  if (progress < 25) {
    statusText = `${progress}% - –ü–µ—Ä—Å–æ–Ω–∞–∂ —â–µ –Ω–µ –∑–º—ñ–Ω—é—î –¥—É–º–∫—É`;
  } else if (progress < 50) {
    statusText = `${progress}% - –ü–µ—Ä—Å–æ–Ω–∞–∂ –ø–æ—á–∏–Ω–∞—î –ø—Ä–∏—Å–ª—É—Ö–∞—Ç–∏—Å—è`;
  } else if (progress < 75) {
    statusText = `${progress}% - –í–∏ —Å–ø—Ä–∞–≤–ª—è—î—Ç–µ –≤—Ä–∞–∂–µ–Ω–Ω—è!`;
  } else {
    statusText = `${progress}% - –í–∏ –±–ª–∏–∑—å–∫—ñ –¥–æ —É—Å–ø—ñ—Ö—É!`;
  }

  progressText.textContent = statusText;

  // –ü—ñ–¥—Å–≤—ñ—Ç–∫–∞ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–∏—Ö –∫–ª—é—á–æ–≤–∏—Ö —Å–ª—ñ–≤
  const hintItems = modal.querySelectorAll(".hint-item");
  keywords.forEach((keyword, index) => {
    let isUsed = false;
    chatBots[botId].memory.forEach((message) => {
      if (
        message.sender === "user" &&
        message.text.toLowerCase().includes(keyword.toLowerCase())
      ) {
        isUsed = true;
      }
    });

    if (isUsed && index < hintItems.length) {
      hintItems[index].classList.add("hint-used");
    }
  });
}

