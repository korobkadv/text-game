document.addEventListener("DOMContentLoaded", () => {
  // –î–æ–¥–∞—î–º–æ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è –ø—ñ–¥–∫–∞–∑–æ–∫ –ø—ñ—Å–ª—è –Ω–∞–¥—Å–∏–ª–∞–Ω–Ω—è –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è
  // –°–ø–æ—Å—Ç–µ—Ä—ñ–≥–∞—î–º–æ –∑–∞ –∑–º—ñ–Ω–∞–º–∏ –≤ —á–∞—Ç–∞—Ö —ñ –æ–Ω–æ–≤–ª—é—î–º–æ –ø—ñ–¥–∫–∞–∑–∫–∏ –∫–æ–ª–∏ –∑'—è–≤–ª—è—î—Ç—å—Å—è –Ω–æ–≤–µ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è
  const chatObserver = new MutationObserver((mutations) => {
    mutations.forEach((mutation) => {
      if (mutation.type === "childList" && mutation.addedNodes.length > 0) {
        // –ó–Ω–∞—Ö–æ–¥–∏–º–æ ID –±–æ—Ç–∞ –∑ –±–∞—Ç—å–∫—ñ–≤—Å—å–∫–æ–≥–æ –µ–ª–µ–º–µ–Ω—Ç–∞
        const chatContainer = mutation.target;
        const tabContent = chatContainer.closest(".tab-content");
        if (tabContent) {
          const botId = tabContent.id;
          setTimeout(() => {
            updateHintProgress(botId);
          }, 1000);
        }
      }
    });
  });

  // –ü—ñ–¥–∫–ª—é—á–∞—î–º–æ —Å–ø–æ—Å—Ç–µ—Ä—ñ–≥–∞—á–∞ –¥–æ –≤—Å—ñ—Ö –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ñ–≤ —á–∞—Ç—ñ–≤
  document.querySelectorAll(".chat-container").forEach((container) => {
    chatObserver.observe(container, { childList: true });
  });
});

